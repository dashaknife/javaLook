package views;

import models.*;

public class ConsoleView implements View{
    public void displayElementOfClothes (ElementOfClothes elementOfClothes) {
        StringBuilder sb = new StringBuilder();
        System.out.println(sb.append(elementOfClothes.getSize()).append(" ")
                .append(elementOfClothes.getColor()).append(" ")
                .append(elementOfClothes.getBrand()));
    }
    public void displayUpperClothes (UpperClothes upperClothes) {
        System.out.println("Upper Clothes: ");
        displayElementOfClothes(upperClothes);
        System.out.println("Type of upper clothes: " + upperClothes.getTypeOfUpperClothes()+ "\n");
    }
    public void displayDownClothes (DownClothes downClothes) {
        System.out.println("Down Clothes: ");
        displayElementOfClothes(downClothes);
        System.out.println("Type of down clothes: "+ downClothes.getTypeOfDownClothes());
    }
    public void displayLook (Look look) {
        System.out.println(look.getName() + "\nPrice: " + look.getPrice());
        displayUpperClothes(look.getUpperClothes());
        displayDownClothes(look.getDownClothes());
        System.out.println("_______________________________________");
    }
    public void displayWardrobe (Wardrobe wardrobe) {
        for (Look look : wardrobe.getLooks()) {
            displayLook(look);
        }
    }
}
